#include "../../headers/vector.h"

t_vec	vec_cross(t_vec u, t_vec v)
{
	return (vec(u.y * v.z - u.z * v.y,
			u.z * v.x - u.x * v.z,
			u.x * v.y - u.y * v.x));
}
